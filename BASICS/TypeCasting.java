// type casting
public class TypeCasting {
    public static void main(String [] args) {
        // implicit typecasting
        int x =10;
        double y=x;
        System.out.println(y);

        // explicit typecasting 
        double a =10;
        int b = (int)a;
        System.out.println(b);
        
        //final keyword
        final int num = 10; //you cant change it
        //num+=10;
        System.out.println(num);

        
    }
}
